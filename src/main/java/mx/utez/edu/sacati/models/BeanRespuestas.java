package mx.utez.edu.sacati.models;

public class BeanRespuestas {

    private int id;
    private int respFun;
    private int respCon;
    private int respUsa;
    private int respRen;
    private int respMan;
    private int resPor;
    private int respSeg;
    private int respCom;
    private double porcentaje;


    public BeanRespuestas() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getRespFun() {
        return respFun;
    }

    public void setRespFun(int respFun) {
        this.respFun = respFun;
    }

    public int getRespCon() {
        return respCon;
    }

    public void setRespCon(int respCon) {
        this.respCon = respCon;
    }

    public int getRespUsa() {
        return respUsa;
    }

    public void setRespUsa(int respUsa) {
        this.respUsa = respUsa;
    }

    public int getRespRen() {
        return respRen;
    }

    public void setRespRen(int respRen) {
        this.respRen = respRen;
    }

    public int getRespMan() {
        return respMan;
    }

    public void setRespMan(int respMan) {
        this.respMan = respMan;
    }

    public int getResPor() {
        return resPor;
    }

    public void setResPor(int resPor) {
        this.resPor = resPor;
    }

    public int getRespSeg() {
        return respSeg;
    }

    public void setRespSeg(int respSeg) {
        this.respSeg = respSeg;
    }

    public int getRespCom() {
        return respCom;
    }

    public void setRespCom(int respCom) {
        this.respCom = respCom;
    }

    public double getPorcentaje() {
        return porcentaje;
    }

    public void setPorcentaje(double porcentaje) {
        this.porcentaje = porcentaje;
    }
}
