package mx.utez.edu.sacati.models;

public class BeanTiendita {

    private String id;
    private String nombre;


    public BeanTiendita() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
